% example_usage.m
% Example usage of the binomial_coefficient function

% Parameters for binomial coefficient
n = 5; % Total number of items
k = 2; % Number of items to choose

% Calculate the binomial coefficient
result = binomial_coefficient(n, k);

% Display the result
fprintf('Binomial coefficient C(%d, %d) is: %d\n', n, k, result);
